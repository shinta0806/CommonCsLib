using System;

namespace Shinta
{
	// ========================================================================
	// public 列挙子
	// ========================================================================

	// ------------------------------------------------------------------------
	// エラーコード
	// ------------------------------------------------------------------------
	public enum StatusT
	{
		GeneralErrorBase = Int32.MinValue,

		// ユーザー定義エラーコードは ErrorsEnd + 1 から始まる
		ErrorsEnd = GeneralErrorBase + 0xffff,

		// 一般的なエラー
		Error = -1,

		// 正常終了
		Ok = 0,
	}
}

